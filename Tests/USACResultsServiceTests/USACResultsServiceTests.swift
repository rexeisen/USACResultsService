    func testDecodedArray() throws {
        struct Student: Decodable {
            let firstName: String
            let lastName: String
        }
        
        let jsonString = """
        {
          "S001": {
            "firstName": "Tony",
            "lastName": "Stark"
          },
          "S002": {
            "firstName": "Peter",
            "lastName": "Parker"
          },
          "S003": {
            "firstName": "Bruce",
            "lastName": "Wayne"
          }
        }
        """

        let jsonData = Data(jsonString.utf8)

        // Ask JSONDecoder to decode the JSON data as DecodedArray
        let decodedResult = try JSONDecoder().decode(DecodedArray<Student>.self, from: jsonData)
        
        XCTAssertEqual(decodedResult.array.count, 3)

    }
    
